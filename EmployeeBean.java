package com.cts;

public class EmployeeBean {
	int eno;
	String name;
	float salary;
	public final int getEno() {
		return eno;
	}
	public final void setEno(int eno) {
		this.eno = eno;
	}
	public final String getName() {
		return name;
	}
	public final void setName(String name) {
		this.name = name;
	}
	public final float getSalary() {
		return salary;
	}
	public final void setSalary(float salary) {
		this.salary = salary;
	}
	
	}


